    private void ButtonConvertMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ButtonConvertMouseClicked
        String url = URL.getText().trim();
        if (!url.isEmpty()) {
            status.append("Processing URL: " + url + "\n");
            try {
                String command = "\"C:\\Users\\Laptop\\OneDrive\\Máy tính\\YT\\yt-dlp.exe\" -x --audio-format mp3 --ffmpeg-location \"C:\\Users\\Laptop\\OneDrive\\Máy tính\\YT\\ffmpeg-7.1-essentials_build\\bin\" " + url;

                Process process = Runtime.getRuntime().exec(command);

                BufferedReader reader = new BufferedReader(new InputStreamReader(process.getInputStream()));
                String line;
                while ((line = reader.readLine()) != null) {
                    status.append(line + "\n");
                }
                reader.close();
                status.append("Download completed!\n");
            } catch (Exception ex) {
                status.append("Error: " + ex.getMessage() + "\n");
            }
        } else {
            status.append("Please enter a valid URL!\n");
        }
        URL.setText("");
    }//GEN-LAST:event_ButtonConvertMouseClicked

